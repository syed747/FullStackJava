package KnightingaleWeek2;

public class ConditionDemo2 {
	 public static void main(String[] args) {
			int i=200;
			
			switch(i)
			{
			    case 100:{
			    	System.out.println("i value is 100");
				    break;
			    }
		    	case 200:{
		     		System.out.println("i value is 200");
		     		break;
			    } 
			    default:{
		            System.out.println("no condition is satisfied.... ");
			}
			}
	 }
}